package staff.model.interfaces;

/**
 * Interface WorkTime contains methods for starting and ending work.
 * @author Valentin Velichko
 * */
public interface WorkTime {

    /**
     * This method describes employee logic to get started.
     * */
    void startWork();

    /**
     * This method describes the employee's logic for finishing work.
     * */
    void endWork();
}
